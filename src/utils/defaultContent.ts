export function cleanName(raw: string): string {
  if (!raw) return "";
  let name = raw
    .replace(/se\s+chama\s+/gi, "")
    .replace(/é\s+/gi, "")
    .replace(/chamad[oa]\s+/gi, "")
    .replace(/nome\s+é\s+/gi, "")
    .replace(/especialista\s+é\s+/gi, "")
    .replace(/^o\s+|^a\s+/gi, "")
    .trim();
  return name.split(" ").map(w => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase()).join(" ");
}
